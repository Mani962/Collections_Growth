package com.collections.collectionspractice.withoutgenerics.list;

public interface ListDetails {

// A List represents an ordered or sequenced group of elements.
// It may contain duplicate elements.
// It extends the collection interface.

//    Note:
//     1. Elements can be inserted or retrieved by their position in the listGenerics.
//     Position or index value starts from 0.
//     2. List interface defines its own methods in addition to the methods of collection interface.

//    -------------------------------------------------

//    Types in List Interface

//   1.ArrayList:
//    ArrayList class extends AbstractList and implements the List interface.
//    It uses dynamic arrays for storing elements.
//    It maintains insertion order.
//    ArrayList can contain duplicate elements.
//    It is not synchronized.

//    Note:
// ArrayList provides the facility of random access because of it is index based.

//    ------------------------------------------------------

//   2.LinkedList
//The LinkedList class extends AbstractSequentialList and implements the List and Deque interface.
// It uses linked listGenerics data structure to store elements.
// It can contain duplicate elements.
// It is not synchronized.

//    Note:
// It does not provides the random access facility.

//    -----------------------------------------------------------
//    3.Vector
//The java.util.Vector class implements a growable array of objects.
// Like an array, it contains components that can be accessed using an integer index.
// However, the size of a Vector can grow or shrink as needed
// to accommodate adding and removing items after the Vector has been created.
// It extends AbstractList and implements List interfaces.




}

