package com.collections.collectionspractice.withoutgenerics.Set;

public interface SetDetails {

// * SetGenerics Interface
//    A set represents a group of elements which can’t contain a duplicate element.
// It extends the collection interface.

//    Note:
// SetGenerics interface contains only methods inherited from Collection interface and
// adds the restriction that can’t contain a duplicate element.


//    Types of SetGenerics Interface

//    1.HashSet

// HashSet extends AbstractSet and implements the SetGenerics interface.
// It not maintains any order for its elements.
// It uses hash table for storage.

//2.LinkedHashSet
//  LinkedHashSet extends HashSet and implements the SetGenerics interface.
// It maintains insertion order for its elements.

//    3.TreeSet
//TreeSet extends AbstractSet and implements the NavigableSet interface.
// It maintains ascending order for its elements i.e. elements will be in sorted form.


}


